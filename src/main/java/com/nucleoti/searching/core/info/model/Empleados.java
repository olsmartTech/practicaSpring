package com.nucleoti.searching.core.info.model;

public class Empleados {
    private String trabajadores;
    private String pensionistas;
    private String prestadores_servicio;

    public String getTrabajadores() {
        return trabajadores;
    }

    public void setTrabajadores(String trabajadores) {
        this.trabajadores = trabajadores;
    }

    public String getPensionistas() {
        return pensionistas;
    }

    public void setPensionistas(String pensionistas) {
        this.pensionistas = pensionistas;
    }

    public String getPrestadores_servicio() {
        return prestadores_servicio;
    }

    public void setPrestadores_servicio(String prestadores_servicio) {
        this.prestadores_servicio = prestadores_servicio;
    }
}
